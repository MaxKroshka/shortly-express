//check if username valid characters, and if username exists in our database
exports.hasher = function (password) {
  var salt = bcrypt.genSaltSyn(10);
  var hash = bcrypt.hashSync(password, salt);
  return hash;
};

exports.encrypt = function(password){
  return crypto.createHash('sha1').update(password).digest('hex');
};


exports.checkUser = function(req, res, next) {
  if (req.session.user) {
    next();
  } else {
    req.session.error = 'Access denied!';
    res.redirect('/login');
  }
};

var validUsername = /^[a-zA-Z0-9]/;

exports.isValidUsername = function(username) {
  return username.match(validUsername);
};
